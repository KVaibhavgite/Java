class Switch1
{
public static void main (String[]args)
{
int a=2;
	switch (a)
{
case 1:
{
System.out.println("case 1");
}
case 2:
{
System.out.println("case 2");//case2
}
case 3:
{
System.out.println("case 3");//case3
}
case 4:
{
System.out.println("case 4");//case4
}
default:
{
System.out.println("default");//default
}
}
}
}


